void gpio_init()
{
	RCC->AHB1ENR |= (1<<0);
	GPIOA->MODER &= ~(1<<5);
	GPIOA->MODER |= (1<<5);
}

void delay(int seconds)
{
for(;seconds>0;seconds--)
for(int count1 =0;count1<3000; count1++);
}


int keypad_kbhit()
{
	int col;

//	GPIOC->MODER = 0x00005500; /* make all row pins output */

	GPIOC->BSRR = 0x00F00000; /* drive all row pins low */
	delay(3000); /* wait for signals to settle */

	col = GPIOC->IDR & 0xF; /* read all column pins */

	GPIOC->MODER &= ~0x0000FF00; /* disable all row pins drive */
	if (col == 0x000F) /* if all columns are high */
		return 0; /* no key pressed */

	else
		return 1; /* a key is pressed */
}

void keypad_init()
{
	RCC->AHB1ENR |= (1<<2);
	GPIOC->MODER &= ~(0xFFFF);
// Rows(R1, R2, R3, R4) : output - PC4, PC5, PC6, PC7
	GPIOC->MODER |= (0x5500);

// Columns(C1, C2, C3, C4) : input - PC0, PC1, PC2, PC3


// pullup the input pins
	GPIOC->PUPDR |= (0x55);
}

